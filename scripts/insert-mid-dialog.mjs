let inserted = 0;
let skipped = [];

for (const [slug, d] of Object.entries(DIALOGS)) {
  const file = path.join(POSTS_DIR, slug, "index.mdx");
  if (!fs.existsSync(file)) { skipped.push(slug + "(ファイルなし)"); continue; }
  const src = fs.readFileSync(file, "utf8");
  const lines = src.split("\n");
  const target = "## " + d.h2;
  const idx = lines.findIndex((l) => l.trim() === target);
  if (idx === -1) { skipped.push(slug + "(見出し不一致)"); continue; }
  // 直前10行にすでにCharacterDialogがあればスキップ（二重挿入防止）
  const before = lines.slice(Math.max(0, idx - 10), idx).join("\n");
  if (before.includes("CharacterDialog")) { skipped.push(slug + "(既に会話あり)"); continue; }

  const block = [
    "<CharacterDialog",
    `  workerText="${d.w}"`,
    `  beaverText="${d.b}"`,
    "/>",
    "",
  ];
  lines.splice(idx, 0, ...block);
  fs.writeFileSync(file, lines.join("\n"));
  inserted++;
}

console.log("挿入した記事数:", inserted);
if (skipped.length) console.log("スキップ:", skipped.join(", "));

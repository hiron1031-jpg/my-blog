// year-questions.json を走査して照合
const data = require("../../src/data/quiz/year-questions.json");
function getKey(exam, year, sec){
  return K[exam] && K[exam][year] && K[exam][year][sec];
}
function officialFor(entry, num){
  if(Array.isArray(entry)) return entry[num-1]; // 1始まり配列
  // {seq, multi}
  if(entry.multi && entry.multi[num]) return entry.multi[num];
  const v = entry.seq[num-1];
  return v;
}
let checked=0, mismatches=[], nokey=[];
for(const exam of Object.keys(data)){
  for(const year of Object.keys(data[exam])){
    const secs=data[exam][year];
    if(Array.isArray(secs)) continue;
    for(const sec of Object.keys(secs)){
      const arr=secs[sec];
      if(!Array.isArray(arr)) continue;
      if(sec==="練習"){ continue; } // 練習セットは本番番号と別体系のためスキップ（別途注記）
      const key=getKey(exam,year,sec);
      if(!key){ nokey.push(exam+"/"+year+"/"+sec+" (n="+arr.length+")"); continue; }
      for(const q of arr){
        const num=q.originalNumber||q.number;
        const stored=q.correctIndex+1;
        const off=officialFor(key,num);
        checked++;
        if(off===undefined||off===null){ mismatches.push({id:q.id,num,stored,off:"(表に該当なし)"}); continue; }
        if(Array.isArray(off)){
          if(!off.includes(stored)) mismatches.push({id:q.id,num,stored,off:off.join("/")});
        } else if(off!==stored){
          mismatches.push({id:q.id,num,stored,off});
        }
      }
    }
  }
}
console.log("照合数: "+checked);
console.log("キー未整備(スキップ): "+(nokey.length?nokey.join(" , "):"なし"));
console.log("不一致: "+mismatches.length+" 件");
for(const m of mismatches) console.log("  "+m.id+"  No."+m.num+"  クイズ="+m.stored+"  公式="+m.off);

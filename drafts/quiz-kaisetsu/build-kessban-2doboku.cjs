let added = 0;
for (const q of NEW_QUESTIONS) {
  const sec = data["doboku-2kyu"][q.year][q.section];
  if (!sec) { console.error("節がない:", q.year, q.section); process.exit(1); }
  if (sec.some((x) => x.originalNumber === q.n)) { console.log("既に存在:", q.id, "スキップ"); continue; }
  const item = {
    id: q.id,
    exam: "doboku-2kyu",
    category: q.category,
    question: q.question,
    options: q.options,
    correctIndex: q.correct - 1,
    explanation: q.explanation,
    originalNumber: q.n,
  };
  // originalNumber順の位置に挿入
  let idx = sec.findIndex((x) => x.originalNumber > q.n);
  if (idx === -1) idx = sec.length;
  sec.splice(idx, 0, item);
  added++;
  console.log("追加:", q.id, "正解", q.correct);
}

